package br.unifor.Panels;

import java.awt.Color;

import javax.swing.JPanel;

public class PainelInferior extends JPanel implements IPainel {

	private PainelInferior(){
		config();
	}
	
	private static PainelInferior instance = null;
	public static PainelInferior getInstance(){
		return instance == null ? instance = new PainelInferior() : instance;
	}
	@Override
	public void config() {
		this.setBackground(Color.BLACK);		
	}

}
